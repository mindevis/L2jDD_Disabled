
package org.l2jdd.gameserver.model.actor.instance;

import org.l2jdd.gameserver.enums.InstanceType;
import org.l2jdd.gameserver.model.actor.Creature;
import org.l2jdd.gameserver.model.actor.templates.NpcTemplate;

public class WarehouseInstance extends NpcInstance
{
	public WarehouseInstance(NpcTemplate template)
	{
		super(template);
		setInstanceType(InstanceType.WarehouseInstance);
	}
	
	@Override
	public boolean isAutoAttackable(Creature attacker)
	{
		if (attacker.isMonster())
		{
			return true;
		}
		return super.isAutoAttackable(attacker);
	}
	
	@Override
	public boolean isWarehouse()
	{
		return true;
	}
	
	@Override
	public String getHtmlPath(int npcId, int value, PlayerInstance player)
	{
		String pom = "";
		if (value == 0)
		{
			pom = Integer.toString(npcId);
		}
		else
		{
			pom = npcId + "-" + value;
		}
		return "data/html/warehouse/" + pom + ".htm";
	}
}
